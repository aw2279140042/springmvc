package com.springmvc.entity;

public class TUser {
	private Integer id;
	private String name;
	/**
	 * 获取 id
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}
	/**
	 * 设置 id
	 * @param id the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}
	/**
	 * 获取 name
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * 设置 name
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
}
